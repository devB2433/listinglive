interface MessageTree {
  [key: string]: string | MessageTree;
}

function getMessageByKey(tree: MessageTree, key: string): string | undefined {
  const parts = key.split(".");
  let current: string | MessageTree | undefined = tree;
  for (const part of parts) {
    if (typeof current !== "object" || current === null || !(part in current)) {
      return undefined;
    }
    current = current[part] as string | MessageTree;
  }
  return typeof current === "string" ? current : undefined;
}

function interpolate(template: string, vars?: Record<string, string | number>) {
  if (!vars) return template;
  return template.replace(/\{\{(\w+)\}\}/g, (_, key) => String(vars[key] ?? ""));
}

export function normalizeLocale(value: string | null | undefined): Locale {
  if (value && SUPPORTED_LOCALES.includes(value as Locale)) {
    return value as Locale;
  }
  return DEFAULT_LOCALE;
}

export function getStoredLocale(): Locale {
  if (typeof window === "undefined") return DEFAULT_LOCALE;
  return normalizeLocale(window.localStorage.getItem(LOCALE_STORAGE_KEY));
}

export function setStoredLocale(locale: Locale) {
  if (typeof window === "undefined") return;
  window.localStorage.setItem(LOCALE_STORAGE_KEY, locale);
}

export function t(locale: Locale, key: string, vars?: Record<string, string | number>) {
  const localeMessages = messages[locale] as unknown as MessageTree;
  const fallbackMessages = messages[DEFAULT_LOCALE] as unknown as MessageTree;
  const raw = getMessageByKey(localeMessages, key) ?? getMessageByKey(fallbackMessages, key) ?? key;
  return interpolate(raw, vars);
}

export function translateApiError(code: string, locale: Locale) {
  return (
    getMessageByKey(messages[locale].auth.errors as unknown as MessageTree, code) ??
    getMessageByKey(messages[locale].dashboard.errors as unknown as MessageTree, code) ??
    getMessageByKey(messages[DEFAULT_LOCALE].auth.errors as unknown as MessageTree, code) ??
    getMessageByKey(messages[DEFAULT_LOCALE].dashboard.errors as unknown as MessageTree, code) ??
    undefined
  );
}

export function getSceneTemplateDisplayName(
  translate: (key: string, vars?: Record<string, string | number>) => string,
  templateKey: string,
  fallback: string,
) {
  const key = `dashboard.sceneTemplateNames.${templateKey}`;
  const translated = translate(key);
  return translated === key ? fallback : translated;
}

export function formatDateTime(value: string, locale: Locale) {
  return new Date(value).toLocaleString(locale === "en" ? "en-CA" : "zh-CN", { hour12: false });
}
